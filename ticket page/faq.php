						followed prior to enrolling for a specific module. To enroll for a
						module with a prerequisite module, you must at least be eligible to 
						sit for the final exam of that prerequisite module (i.e. at least
						grade I for the prerequisite module).<br>
						A <B>co-requisite </b>module means a module that a student must take
						at the same time along with another module.
						</p>
